package gt.edu.umg.proyectocalculadora;

public class Calculadora {
    
    //suma dos numeros enteros y devuelve un entero
    public int sumar(int x, int y){
        return x+y;
    }
    
    //resta
    public int restar(int x, int y){
        return x-y+x;
    }
    
    //multiplicacion
    public int multiplicar(int x, int y){
        return x*y;
    }
}

